    public void ReceberLista(Listar a) {
        lista.setCoddvd(a.getCoddvd());
        lista.setCodaluguel(a.getCodaluguel());
        lista.setCodcliente(a.getCodcliente());

       
    }

    public void AtualizarCampos() {
        iptCodAluguel.setText(lista.getCodaluguel());
        iptCodDvd.setText(lista.getCoddvd());
        iptCliente.setText(lista.getCodcliente());
    }

    public void AtualizaDate() {
        Date date = new Date();
        SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat hora = new SimpleDateFormat("HH:mm");
        iptData.setText(data.format(date));
        iptHora.setText(hora.format(date));
    }

    public void AtualizaDvd(String cod) {
        Connection con = Conexao.AbrirConexao();
        DVDDAO sql = new DVDDAO(con);
        List<Dvd_Filme> lista = new ArrayList<>();
        lista = sql.CapturarLoca(Integer.parseInt(cod));

        for (Dvd_Filme tra : lista) {
            
            iptTitulo.setText(tra.getTitulo());
            iptCategoria.setText(tra.getCategoria());
            iptClassificacao.setText(tra.getClassificacao());
            iptValor.setText(tra.getPreco());
            fotoCapa.setIcon(new ImageIcon("/home/matheus/Imagens/Video Locadora/Pictures/" + tra.getCapa() + "/"));
        }
    }

    public void AtualizaCliente() {
        Connection con = Conexao.AbrirConexao();
        ClienteDAO sql = new ClienteDAO(con);
        List<Cliente> lista = new ArrayList<>();
        lista = sql.CapturarCliente(Integer.parseInt(iptCliente.getText()));

        for (Cliente tra : lista) {
            System.out.println(tra.getNome());
            iptCliente.setText(tra.getNome());
           
        }
    }

