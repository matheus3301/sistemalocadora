    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       Connection con = Conexao.AbrirConexao();
       AluguelDAO sql = new AluguelDAO(con);
       
       int idloca = Integer.parseInt(iptCodAluguel.getText());
       
       String res = sql.EfetuarDevolucao(idloca);
        System.out.println(res);
        
        if(res.equals("Devolvido com Sucesso!")){
            
            int iddvd = Integer.parseInt(iptCodDvd.getText());
            sql.AtualizarSituacao("Disponível", iddvd);
            JOptionPane.showMessageDialog(null, "Devolvido com Sucesso!","Sistema Locadora",JOptionPane.INFORMATION_MESSAGE);
            dispose();
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao Devolver","Sistema Locadora",JOptionPane.ERROR_MESSAGE);
            dispose();
            new ConsultaDevolucao().setVisible(true);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

