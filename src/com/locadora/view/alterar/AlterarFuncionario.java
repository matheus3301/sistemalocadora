    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = iptNome.getText();
        String login = iptLogin.getText();
        String senha = iptSenha.getText();
        int cod = Integer.parseInt(iptCod.getText());

        if (nome.equals("") || login.equals("") || senha.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum Campo pode Estar Vazio!", "Video Locadora", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = Conexao.AbrirConexao();
            FuncionarioDAO sql = new FuncionarioDAO(con);

            Funcionario a = new Funcionario();

            a.setNome(nome);
            a.setLogin(login);
            a.setSenha(senha);
            a.setCod(cod);

            String resultado = sql.AlterarFuncionario(a);

            System.out.println(resultado);
            Conexao.FecharConexao(con);

            iptNome.setText("");
            iptLogin.setText("");
            iptSenha.setText("");

            JOptionPane.showMessageDialog(null, "Alteração Realizada com Sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);

            dispose();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

